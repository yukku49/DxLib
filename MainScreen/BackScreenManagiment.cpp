    void BackScreenManagiment::CopyPart(int startX, int startY, int partType)
    {
        if (partType == 1) {
            for (int y = 0; y < 23; y++) {
                for (int x = 0; x < 40; x++) {
                    // 配列の範囲内かチェック（安全策）
                    if (startY + y < MAP_HEIGHT && startX + x < MAP_WIDTH) {
                        m_stageMap[startY + y][startX + x] = statgeaPatern_1[y][x];
                    }
                }
            }
        }
    }

